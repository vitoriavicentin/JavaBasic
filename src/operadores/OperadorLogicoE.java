/**
 * Classe utilizada para demonstrar o uso do operador logico E ( && )
 */
public class OperadorLogicoE {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean c = true;

        System.out.println(a && b);
        System.out.println(a && c);
    }
}
